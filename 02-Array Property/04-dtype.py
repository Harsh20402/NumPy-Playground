import numpy as np

# -----------------------------
# 1D Array
# -----------------------------
arr_1d = np.array([1, 2, 3, 4, 5.6, 6, 7, 8.1])

print('1D Array:')
# Output: 1D Array:
print(arr_1d)
# Output: [1.  2.  3.  4.  5.6 6.  7.  8.1]

print(f'Data type is {arr_1d.dtype}')
# Output: Data type is float64
